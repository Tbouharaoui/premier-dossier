CREATE DATABASE assurauto;

CREATE TABLE CLIENTS(
CL_ID INT NOT NULL PRIMARY KEY,
CL_NOM VARCHAR(30) NOT NULL,
CL_PRENOM VARCHAR(30) NOT NULL,
CL_ADRESSE VARCHAR(100) NOT NULL,
CL_CODEPOSTAL INT(5) NOT NULL,
CL_VILLE VARCHAR(30) NOT NULL,
CL_COORDONNEE INT(20) NOT NULL
);

CREATE TABLE CONTRAT(
CO_ID INT NOT NULL PRIMARY KEY,
CO_DATE DATE NOT NULL,
CO_BONUS INT(100) NOT NULL,
CO_MALUS INT(100) NOT NULL,
CO_CATEGORIE_FK INT NOT NULL,
FOREIGN KEY (CO_CATEGORIE_FK) REFERENCES CLIENTS(CL_ID)
);

